namespace DiGi.UI.WPF.Constants
{
    /// <summary>
    /// Provides constant values for file extensions used within the WPF user interface.
    /// </summary>
    public static class FileExtension
    {
        /// <summary>
        /// The extension for PNG image files.
        /// </summary>
        public const string Png = "png";

        /// <summary>
        /// The primary extension for JPEG image files (.jpg).
        /// </summary>
        public const string Jpeg_1 = "jpg";

        /// <summary>
        /// The secondary extension for JPEG image files (.jpeg).
        /// </summary>
        public const string Jpeg_2 = "jpeg";

        /// <summary>
        /// The tertiary extension for JPEG image files (.jpe).
        /// </summary>
        public const string Jpeg_3 = "jpe";

        /// <summary>
        /// The quaternary extension for JPEG image files (.jfif).
        /// </summary>
        public const string Jpeg_4 = "jfif";
    }
}